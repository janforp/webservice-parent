public class HelloException_Exception extends RuntimeException {

    public HelloException_Exception() {
    }

    public HelloException_Exception(String message) {
        super(message);
    }

    public HelloException_Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public HelloException_Exception(Throwable cause) {
        super(cause);
    }

    public HelloException_Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
